		<h2 id="signing-up-to-volunteer">Signing up to Volunteer</h2>
		<p>
			The Marist School Parents Club holds the Holiday Traditions Event each year with the help of
			hundreds of student and parent volunteers. Volunteer sign up links are coming soon.
		</p>
		<p>Volunteer opportunities include:</p>
		<ul>
			<li>Bake Sale – baking and/or working a shift at the bake sale</li>
			<li>Concessions – setting up the concession stand prior and/or working a shift</li>
			<li>Cooked Goose Café – making food prior and/or working shifts/cashier</li>
			<li>Decorations – set up - shifts on Wednesday or take down on Saturday</li>
			<li>Parking – help direct customers to on campus parking</li>
			<li>Raffle – working/selling raffle tickets at a shift</li>
			<li>
				Shuttle – driving a Marist bus for a shift on Saturday *<em
					>must have copy of driver’s license prior to event</em
				>
			</li>
			<li>
				Ticket Sales – stuffing bags on Friday and/or working shifts at the ticket entry tables on
				Saturday
			</li>
			<li>Vendor Traffic – directing traffic for load in on Friday afternoon</li>
			<li>Taping Vendor Booth Space – Thursday and Friday shifts</li>
		</ul>
